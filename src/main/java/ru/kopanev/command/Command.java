package ru.kopanev.command;

/**
 * Интерфейс команды (паттерн GoF Command).
 * Инкапсулирует действие в виде объекта, что позволяет параметризовать
 * клиентов с различными запросами, ставить запросы в очередь и поддерживать
 * отмену операций.
 *
 * <p>Используется для реализации пунктов меню приложения, где каждая команда
 * представляет отдельное действие пользователя (регистрация, вход, добавление товара и т.д.).</p>
 *
 * @author Artem Kopanev
 * @since 1.0
 * @see <a href="https://refactoring.guru/design-patterns/command">Command Pattern</a>
 */
public interface Command {

    /**
     * Выполняет команду.
     * Каждая реализация определяет своё специфичное действие.
     */
    void execute();
}